    std::unordered_set<unsigned int> search_keys(std::string const &search_term){
        std::vector<Category> category_reduced;
        std::vector<Subcategory> subcategory_reduced;
        std::unordered_set<unsigned int> matches;

        for (auto const item : category_list)  {
           if (item.first.find(search_term) != std::string::npos){
                category_reduced.push_back(item.second);
           }
        }

        for (auto const item : subcategory_list)  {
            if (item.first.find(search_term) != std::string::npos){
                subcategory_reduced.push_back(item.second);
            }
        }

        for (unsigned int i = 0; i<emoji_list.size(); i++){
            auto matched_category = std::find(begin(category_reduced), end(category_reduced), emoji_list[i].category);
            if ( matched_category != category_reduced.end()){
                matches.insert(i);
            }
        }

    return matches;
    }

